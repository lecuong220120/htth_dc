package com.example.demo.NsoObj;

public class User {
    private int id;
    private String username;
    private String fullName;
    private String phoneNumber;
    private String email;
    private long money;
    private int role;
    private String avatar;
    private int chatBlock;
    private int gameBlock;
    private String chatNameColor;
    private String chatArmorial;
    private String lastWithdrawal;
    private String createdAt;
    private String updatedAt;

    // Getters and Setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public long getMoney() {
        return money;
    }

    public void setMoney(long money) {
        this.money = money;
    }

    public int getRole() {
        return role;
    }

    public void setRole(int role) {
        this.role = role;
    }

    public String getAvatar() {
        return avatar;
    }

    public void setAvatar(String avatar) {
        this.avatar = avatar;
    }

    public int getChatBlock() {
        return chatBlock;
    }

    public void setChatBlock(int chatBlock) {
        this.chatBlock = chatBlock;
    }

    public int getGameBlock() {
        return gameBlock;
    }

    public void setGameBlock(int gameBlock) {
        this.gameBlock = gameBlock;
    }

    public String getChatNameColor() {
        return chatNameColor;
    }

    public void setChatNameColor(String chatNameColor) {
        this.chatNameColor = chatNameColor;
    }

    public String getChatArmorial() {
        return chatArmorial;
    }

    public void setChatArmorial(String chatArmorial) {
        this.chatArmorial = chatArmorial;
    }

    public String getLastWithdrawal() {
        return lastWithdrawal;
    }

    public void setLastWithdrawal(String lastWithdrawal) {
        this.lastWithdrawal = lastWithdrawal;
    }

    public String getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(String createdAt) {
        this.createdAt = createdAt;
    }

    public String getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(String updatedAt) {
        this.updatedAt = updatedAt;
    }
}
